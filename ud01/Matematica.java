// Crear programa que:
// Calcule raíz cuadrada de 234324
// Calcule 34 elevado a 17

public class Matematica {

    public static void main(String[] args) {
        double raiz = Math.sqrt(234324);
        double potencia = Math.pow(34, 17);
        
        System.out.println("A raíz cuadrada é " + raiz);
        System.out.println("34 elevado a 17 é " + potencia);
    }
}